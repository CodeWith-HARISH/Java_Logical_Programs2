package com.Test1_logics;

public class Largest_number_from_ARRAY {

	public static void main(String[] args) {
		
		int[] arr= {10,20,50,80,70};
		
		int max=arr[0];
		
		for(int i=0;i<arr.length;i++)
		{
			if(arr[i]>max) 
			{
				max=arr[i];
				
			}
				
		}
		
		System.out.println("Largest_number_form_givern_Array_is:"+max);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
